
public class OperadorTernario {

	public static void main(String[] args) {
		//Ternario Condicional
		int num1 = 3, num2 = 0, resultado = 0;
		//A la variable, si la condicion es true se le asigna el primer elemento
		//si es false, se le asigna el 2o elemento
		resultado = (num2 > num1 ? num2 : num1);
		
		System.out.println(resultado);
	}

}
